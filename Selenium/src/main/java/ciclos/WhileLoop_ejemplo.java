package ciclos;

public class WhileLoop_ejemplo {

	public static void main(String[] args) {
		int a = 1;
		
		//va salir del ciclo while "a" es mayor que 4
		while (a <= 4) {
			System.out.println("el valor de a: " + a);
			a++;//incrementa el valor de 1 para la sigueiente iteracion
			
		}
		
	}

}
